/**
 * Get category icon component based on category name
 */
export const getCategoryIconComponent = (category: string): React.ReactNode => {
 const categoryMap: { [key: string]: React.FC } = {
  Food: FoodIcon,
  Fashion: FashionIcon,
  Services: ServicesIcon,
  Tech: TechIcon,
  Medicine: MedicineIcon,
 };

 const IconComponent = categoryMap[category];
 return IconComponent ? <IconComponent /> : <ServicesIcon />;
};

/**
 * Get feature icon component based on feature name
 */
export const getFeatureIconComponent = (featureName: string): React.ReactNode => {
 const featureMap: { [key: string]: React.FC } = {
  search: SearchIcon,
  verified: VerifiedIcon,
  chat: ChatIcon,
  mobile: MobileIcon,
  security: SecurityIcon,
  location: LocationIcon,
  time: TimeIcon,
  feedback: FeedbackIcon,
 };

 const IconComponent = featureMap[featureName.toLowerCase()];
 return IconComponent ? <IconComponent /> : <SearchIcon />;
};
